#include "ft_printf.h"

int ft_setcolor(char *str, size_t *i)
{
    char    color[8];
    int     j;
    
    (*i)++;
    j = 0;
	while (str[*i] != '}')
    {
        (str[*i]) = color[j];
        i++;
        j++;
    }
    color[j] = '\0';
    if(!strcmp(*color, "blue"))
        ft_putstr("\e[00;34m");
    return (0);
}